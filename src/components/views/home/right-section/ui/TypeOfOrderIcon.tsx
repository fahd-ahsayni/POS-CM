export function TypeOfOrderDescription({ type }: { type: string }) {
  const generalData = JSON.parse(localStorage.getItem("generalData") || "{}");
  const orderTypes = generalData.orderTypes || [];
  const typeOfOrder = orderTypes.find(
    (order: any) => order.type.toLowerCase() === type
  );

  if (!typeOfOrder) return null;

  switch (typeOfOrder.type.toLowerCase()) {
    case "onplace":
      return "Dine in at the restaurant";
    case "takeaway":
      return "Pick up your order at the location";
    case "delivery":
      return "We'll deliver to your specified address";
    default:
      return null;
  }
}
